package ControlFLowAndConditionals;

public class ElseStatement {
    public static void main(String[] args) {
        boolean isLightGreen = false;

        if (isLightGreen) {
            //traffic light is green.
            System.out.println("Drive!");
        } else {
            //light is NOT green.
            System.out.println("Stop!");
        }
    }
}
